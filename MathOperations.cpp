#include "MathOperations.h"

MathOperations::MathOperations() {
    // Empty Constructor
}

int MathOperations::sum(int a, int b) {
    return a + b;
}

int MathOperations::subtract(int a, int b) {
    return a - b;
}

int MathOperations::mult(int a, int b) {
    return a * b;
}

int MathOperations::divide(int a, int b) {
    return a / b;
}